package Parseur;

public class Triple {

    private float n1;
    private float n2;
    private float n3;

    public Triple(float n1, float n2, float n3) {
        this.n1 = n1;
        this.n2 = n2;
        this.n3 = n3;
    }

    public String toString(){

        return "" + this.n1 + ":" + this.n2 + ":" + this.n3;

    }





}
